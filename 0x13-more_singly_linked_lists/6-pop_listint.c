/**
 * pop_listint - deletes the head node of
 * a linked list
 * @head: head of a list.
 *
 * Return: head node's data.
 */
int pop_listint(listint_t **head)
{
	int hnode;
	listint_t *h;
	listint_t *curr;

	if (*head == NULL)
		return (0);

	pointer = *head;

	hnode = pointer->n;

	h = pointer->next;

	free(pointer);

	*head = h;

	return (hnode);
}
